package com.hl.web.form;

public class AbstractInfoForm {

	private String month;
	private String fee;
	private String volumeBegin;
	private String volumeEnd;
	
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
	public String getVolumeBegin() {
		return volumeBegin;
	}
	public void setVolumeBegin(String volumeBegin) {
		this.volumeBegin = volumeBegin;
	}
	public String getVolumeEnd() {
		return volumeEnd;
	}
	public void setVolumeEnd(String volumeEnd) {
		this.volumeEnd = volumeEnd;
	}
	
	
	
}
